

# Poproś użytkownika o wprowadzenie słowa
# i przypisz je do zmiennej slowo_uzytkownika

slowo = input("podaj slowo")
slowo = slowo.upper()
# Uzupełnij pętlę for poniżej:
for litera in slowo:
    if litera == "A":
        continue
    elif litera == "E":
        continue
    elif litera == "I":
        continue
    elif litera == "O":
        continue
    elif litera == "U":
        continue
    else:
        print(litera)
